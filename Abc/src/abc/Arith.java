package abc;

public class Arith {
	public static void main (String[]args) {
		int a=100;
		int b=10;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a%b);
		System.out.println(a/b);
		++a;
		System.out.println(a);
		b--;
		System.out.println(b);
		int c=12;
		c+=1;
		System.out.println(c);
		c-=1;
		System.out.println(c);
		c*=1;
		System.out.println(c);
		c/=2;
		System.out.println(c);
		c%=2;
		System.out.println(c);
		
	}

}
